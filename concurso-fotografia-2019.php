	<section class="w-100 flex justify-content-center mt-10 mb-14">
		<div class="col-xl-10 col-lg-10 col-md-10 col-sm-10 col-xs-12 flex">
			<div class="w-100 flex">
				<h2 class="title text-blue_1 w-100">Primer Concurso de Fotografía Digital 2019</h2>
				<h2 class="title text-blue_1 w-100">"Ponle Imagen a la Anticorrupción"</h2>
			</div>
			<div class="w-100 mt-5 mb-5 flex">

				<p class="paragraph-md text-blue_1 p-2">En el marco de la Semana Anticorrupción que se celebra del 3 al 9 de diciembre de 2019, el Sistema Estatal
				Anticorrupción de Yucatán convoca a su Primer Concurso de Fotografía Digital cuyo objetivo es que el fotógrafo logre transmitir en su pieza fotográfica acciones, valores, actitudes o momentos que comuniquen anticorrupción
				</p>
				<p class="paragraph-md text-blue_1 p-2"><span class="bold w-100">BASES.</span>
				Podrán participar todos los ciudadanos que deseen, a partir de los 18 años cumplidos, residentes en el Estado de Yucatán, y no tener ninguna relación familiar consanguínea con algún miembro del jurado o con cualquiera de los integrantes del Sistema Estatal Anticorrupción.</p>
				<p class="paragraph-md text-blue_1 p-2"><span class="bold w-100">TEMA ANTICORRUPCION</span>
					Las imágenes fotográficas deberán de comunicar en su concepción y realización final, actitudes, acciones o
					momentos que expresen valores como: honestidad, integridad, justicia, legalidad, respeto, responsabilidad social, solidaridad… <br>
					Buscamos la mejor imagen que represente la anticorrupción, queremos ponerle imagen a la anticorrupción.</p>
				<p class="paragraph-md text-blue_1 p-2"><span class="bold w-100">MECANICA DE PARTICIPACION</span>
					Enviar al correo electrónico <span>concursodefotografia@seay.org.mx</span> la fotografía que inscribas para participar teniendo en consideración estos puntos:</p>
				<ol class="w-90 paragraph-md text-blue_1 p-2" type="1">
	  					<li>En “Asunto” escribe el <span>título de tu fotografía</span> seguido de tu <span>seudónimo.</span></li>
	  					<li>En el cuerpo del texto del correo electrónico, escribe el <span>nombre</span> del participante, un número telefónico de <span>contacto</span>, más una breve <span>descripción, explicación y/o justificación</span> de esa imagen con respecto al tema <span>“Anticorrupción”</span>. Importante: buscamos una imagen que exprese <span>anticorrupción</span>, no corrupción: de esta hay muchas.</li>
	  					<li>Se deberá incluir en un <span>archivo anexo</span> la imagen a participar en formato <span>JPG</span>, con un peso <span>máximo de 5 mega pixeles.</span> Es importante nombrar dicho archivo con el mismo <span>pseudónimo que escribiste en “Asunto”</span> para que el jurado calificador desconozca los nombres reales de los participantes y a la vez se identifique plenamente tu fotografía con tu seudónimo. Se aceptará un máximo de dos archivos por persona.</li>
	  					<li>La fecha límite para recibir correos electrónicos es el <span>viernes 29 de noviembre a las 12 del día.</span> Los correos que ocasionalmente pudieran llegar después de esa fecha no participarán en el concurso.</li>
	  					<li>Las fotografías deberán ser propias del participante, inéditas, originales, no tomadas de ninguna otra imagen o banco de imágenes y no haberse presentado con anterioridad en ningún otro evento, exposición o concurso.</li>
	  					<li>Todas las fotografías enviadas para el concurso serán donadas por los autores y pasarán a ser propiedad del SESEAY, por lo que no existirá ningún compromiso posterior en caso de ser utilizadas en campañas del Sistema Estatal Anticorrupción a través de cualquier medio de difusión, impreso o digital.</li>
	  					<li>Sólo se concursará en una sola categoría: color</li>
	  					<li>Cualquier omisión en el cumplimento de las bases aqui descritas, será motivo de descalificación.</li>
				</ol>
				<p class="paragraph-md text-blue_1 p-2"><span class="bold w-100">MECANICA DE CALIFICACION</span></p>
				<ol class="w-90 paragraph-md text-blue_1 p-2" type="1">
	  					<li>El <span>Sistema Estatal Anticorrupción de Yucatán (SEAY)</span> hará acopio de todos los archivos recibidos y, cuidando el anonimato, los distribuirá al jurado calificador para realizar la deliberación.</li>
	  					<li>El jurado seleccionará las mejores 10 fotografías de todas las que llegaron y se subirán a la página del facebook del Comité de Participación Ciudadana: @cpcyucatan.org.mx el día 2 de diciembre de 2019</li>
	  					<li>A partir de que las 10 fotos finalistas estén publicadas en la página del facebook del CPC el público podrá votar dándole “like” a la foto de su preferencia. La foto con mayor número de “likes” será la fotografía ganadora. Se premiarán también al segundo y tercer lugar, también en relación a los “likes” recibidos. El día límite para darle “like” a las fotografías que concursan será el sábado 7 de diciembre a las 12 del día.</li>
	  					<li>El SEAY contactará vía telefónica a los 10 finalistas para que envíen, a más tardar el martes 3 de diciembre, el archivo original a un impresor previamente acreditado por el propio SEAY</li>
	  					<li>El SEAY, exibirá las 10 fotos finalistas en un evento del 9 de diciembre y dará a conocer el resultado oficial de la votación otorgando los premios a los tres primeros lugares. Al primer lugar se le reconocerá como el autor de “La Imagen Anticorrupción 2019”</li>
				</ol>
				<p class="paragraph-md text-blue_1 p-2"><span class="bold w-100">JUECES.</span>
					La revisión y evaluación de los trabajos, la realizará un Jurado Calificador de 5 especialistas en la materia calificando aspectos creativos, técnicos, estéticos y principalmente su valor de expresión y representatividad de <span>anticorrupción</span></p>
				<p class="paragraph-md text-blue_1 p-2"><span class="bold w-100">PREMIOS.</span>
					La premiación se realizará el lunes 9 de diciembre en el Edificio del Tribunal Superior de Justicia del Estado de Yucatán, en la Av. Jacinto Canek 90, Centro, 97069 Mérida Yuc. durante la Sesión Conmemorativa por el Día Internacional contra la corrupción y el primer aniversario de la puesta en marcha del SEAY. Cualquier situación no prevista en estas bases, será resuelta por el Comité de Participación Ciudadana del SEAY.</p>
