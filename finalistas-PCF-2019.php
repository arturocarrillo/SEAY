      <div class="w-100 mt-5 mb-5 flex">
        <p class="paragraph-md text-blue_1 p-2">
         Entre el 12 de noviembre y el 07 de diciembre de 2019, el Sistema Estatal Anticorrupción en el marco de la Semana Anticorrupción, realizó el primer Concurso de fotografía “Ponle Imagen a la Anticorrupción”. 
        </p>
        <p class="paragraph-md text-blue_1 p-2">
         A la SESEAY llegaron 31 fotografías vía electrónica, las cuales después de que un jurado calificador de expertos seleccionara las que consideraron las mejores 10, se expusieron en la página de Facebook del Comité de Participación Ciudadana en donde el público pudo de elegir a las tres fotografías ganadoras. El jurado calificador estuvo integrado por:
        </p>
        <ol class="w-80 paragraph-md text-blue_1 p-2">
            <li>Br. Pamela Basto Argüelles</li>
            <li>Lic. Salvador Augusto Ávila Arjona</li>
            <li>Arq. Manuel J. Castillo Rendón</li>
            <li>Mtro. Leobardo Rafael Espinosa Rendón</li>
        </ol>
        <p class="paragraph-md text-blue_1 p-2">
            Los participantes ganadores fueron:
        </p>
       <ol class="w-80 paragraph-md text-blue_1 p-2">
            <li>Primer Lugar: Jaime Arturo Maldonado Quiñones con “No más maquillaje”</li>
            <li>Segundo y tercer lugar: Christian Camacho Clavijo con:</li>
            <li>Segundo Lugar: “No todas son risas”</li>
            <li>Tercer Lugar: “Buscamos tierras de libertad”</li>
        </ol>
        <p class="paragraph-md text-blue_1 p-2">
          Las fotografías finalistas estuvieron exhibidas durante la semana Anticorrupción en el Centro Cultural Olimpo, acompañado del crédito correspondiente al autor.
